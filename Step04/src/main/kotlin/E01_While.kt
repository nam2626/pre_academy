fun main() {
    /*
        *       While 문
        *           - 조건이 참인 동안 반복
        *           - 조건이 거짓이면 반복문을 빠져나옴
        *
        *       while(조건식) {
        *           // 실행문
        *       }
        * */
//    1~10까지 출력
    var i = 1

    while(i <= 10){
        print("$i ")
        i++
    }
}